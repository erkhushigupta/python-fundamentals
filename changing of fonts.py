
#Performing changes on the fixed string

quote = "I think there is a world market for maybe five computers."


print("Original quote:")


print(quote)

print("\nIn uppercase:")

print(quote.upper())

print("\nIn lowercase:")

print(quote.lower())

print("\nAs a title:")

print(quote.title())

print("\nWith a minor replacement:")

print(quote.replace("five", "millions of"))

print("\n Original quote is still:")

print(quote)

input("\n\n Press the enter key to exit.")

#Expected output:
#Original quote:
#I think there is a world market for maybe five computers.

#In uppercase:
#I THINK THERE IS A WORLD MARKET FOR MAYBE FIVE COMPUTERS.

#In lowercase:
#i think there is a world market for maybe five computers.

#As a title:
#I Think There Is A World Market For Maybe Five Computers.

#With a minor replacement:
#I think there is a world market for maybe millions of computers.

#Original quote is still:
#I think there is a world market for maybe five computers.
